package day01;

public class Pratice01 {

	/*
	 Primitive DataType
	 boolean, char,byte, short , int, long, float, double 
	 */
	public static void main(String[] args) {
		

		boolean booleandegiskeni = true; //false
		char chardegiskeni = 'a';
		System.out.println(chardegiskeni);
		
		//How can you print min and max values ​​of byte, short,
		//int, long, float, double.
		System.out.println("ismim hamza");
		System.out.println(booleandegiskeni);//true
		
		System.out.println(Byte.MIN_VALUE);//-128
		System.out.println(Byte.MAX_VALUE);//127
		
		System.out.println(Short.MIN_VALUE);//-32768
		System.out.println(Short.MAX_VALUE);//32767
		
		System.out.println(Integer.MIN_VALUE);//-2147483648
		System.out.println(Integer.MAX_VALUE);//2147483647
		
		System.out.println(Long.MIN_VALUE);//-9223372036854775808
		System.out.println(Long.MAX_VALUE);//9223372036854775807
		
		System.out.println(Float.MIN_VALUE);//1.4E-45
		System.out.println(Float.MAX_VALUE);//3.4028235E38
		
		System.out.println(Double.MIN_VALUE);//9E-324
		System.out.println(Double.MAX_VALUE);//7976931348623157E308
		//Double and Float are decimal numbers.
		
		//"\n" alt satira yazdirir.
	}

}
